-- Query para generar fechas aleatorias entre '2024-01-01' y '2024-12-31'
SET @start_date = '2025-01-01';
SET @end_date = '2025-04-30';

INSERT INTO InsumoxIps (idInsumo, idIps, fechaEntrega, estadoEntrega) 
SELECT 
    idInsumo,
    idIps,
    DATE_ADD(@start_date, INTERVAL FLOOR(RAND() * DATEDIFF(@end_date, @start_date)) DAY) AS fechaEntrega,
    CASE WHEN RAND() > 0.5 THEN 'Entregado' ELSE 'Pendiente' END AS estadoEntrega
FROM
    (SELECT 
        Insumo.idInsumo,
        Ips.idIps
    FROM 
        Insumo
    CROSS JOIN Ips
    ORDER BY RAND()
    LIMIT 40) AS subquery;
    
